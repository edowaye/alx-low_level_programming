/**
  *factorial - Function prototype
  *
  *Description: This program writes a function
  *that eturns the factorial of a given number n
  *@n: parameter
  *Return: an integer
  */

int factorial(int n)
{
	if (n < 0)
		return (-1);

	if (n <= 1)
		return (1);
	else
		return (n * factorial(n - 1));
}
